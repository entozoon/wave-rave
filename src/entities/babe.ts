class Babe extends Sprite {
  constructor({ sprite }) {
    const texture = Texture.from(sprite);
    super(texture);
    this.anchor.set(0.5);
    this.width = 20;
    this.height = 20;
    this.x = 20;
    this.y = 20;
    this.tint = 0x00ff00;
    pixi.containerMain.addChild(this);
  }
  update() {
    console.log("babe update");
    this.x += 0.02;
  }
}
export default new Babe({ sprite: "./babe.png" });
